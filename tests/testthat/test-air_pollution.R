test_that("create_air_pollution_lags handles unsorted rows and date gaps correctly", {
  # Unsorted within region
  CAPL_TEST_DATA_UNSORTED <- data.frame(
    date = as.Date(c("2024-01-03","2024-01-01","2024-01-02","2024-01-04")),
