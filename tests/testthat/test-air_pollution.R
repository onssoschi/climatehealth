  expect_equal(res1$pm25_lag1, c(10,20,30))

  # Date gaps (lag based on row order)
  CAPL_TEST_DATA_GAPS <- data.frame(
    date = as.Date(c("2024-01-01","2024-01-10","2024-01-20","2024-01-21")),
    region = "A",
    pm25 = c(10, 20, 30, 40)
